import java.util.HashSet;



public class O3_Hashset {
	
	public static void main(String[] args) {
		
		HashSet<String> st = new HashSet<String>();
		
		st.add("Java");
		st.add("Python");
		st.add("SQL");
		st.add("10");
		
		for(String s : st)
		{
			System.out.println(s);
		}
		
		
		
		
		
		
		
	}

}
